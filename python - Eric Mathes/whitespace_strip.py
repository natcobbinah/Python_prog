favourite_language = 'python '
print(favourite_language)
favourite_language.rstrip()
print(favourite_language)

#permanent whitespace strip
favourite_language = favourite_language.rstrip()
print(favourite_language)

#left strip
fav_lang = ' python'
print(fav_lang)
fav_lang = fav_lang.lstrip()
print(fav_lang)

# both sides strip
fav_lang2 = ' python '
print(fav_lang2)
fav_lang2 = fav_lang2.strip()
print(fav_lang2)