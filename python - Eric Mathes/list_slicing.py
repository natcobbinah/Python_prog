players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3])

# slice from 2nd to 4th
print(players[1:4])

# first index omitted starts slicing from zero
print(players[:4])

# end index omitted starts for start index to the end of th elist
print(players[2:])

# slicing from th eend of the list
print(players[-3:])

# skipping values whiles slicing
print(players[1:4:2])